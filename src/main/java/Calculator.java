public class Calculator {

  public static int add(final int a, final int b) {
    return a + b;
  }

  public static int subtractBFromA(final int a, final int b) {
    return a - b;
  }

  public static int multiply(final int a, final int b) {
    return a * b;
  }

  public static int divideAByB(final int a, final int b) {
    return a / b;
  }
}
